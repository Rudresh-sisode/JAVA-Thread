package co.edureka.model;

public class AddUser {
	public String name;
	public String date;
	public String address;
	public String email;
	public String accountType;
	
	
	
	public AddUser(String name, String date, String address, String email, String accountType) {
		
		this.name = name;
		this.date = date;
		this.address = address;
		this.email = email;
		this.accountType = accountType;
	}
	
	
	
	

}
